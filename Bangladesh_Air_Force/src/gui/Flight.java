    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        //add new flight 
        try{
            String F_no = fn.getText();
            String name  = n.getText();
            String category= (String)c.getSelectedItem();
            String ss = (String)p.getSelectedItem();
            StringTokenizer st = new StringTokenizer(ss,":");
           String p_name=st.nextToken(); 
           String p_phone=st.nextToken();

            db.DbConnect.st.executeLargeUpdate("insert into flight(f_no,name,category,pilot_name,pilot_phone)values('"+F_no+"','"+name+"','"+category+"','"+p_name+"','"+p_phone+"')");
            JOptionPane.showMessageDialog(null,"Success");
          getEntries();
        }
        catch(SQLIntegrityConstraintViolationException ex){
            JOptionPane.showMessageDialog(null,"Entry Already Exist!");
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

