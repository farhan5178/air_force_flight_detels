     private void getEntries(){
       
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        
        int rc = dtm.getRowCount();
        
        for(int i = 0; i<rc; i++){
            dtm.removeRow(0);
        }
        //add rows from fb to table 
             try{
                 ResultSet rs=     db.DbConnect.st.executeQuery("select * from flight");
                 int x = 0; 
                 while(rs.next()){
                     String f=rs.getString("f_no");
                     String n=rs.getString("name");
                       String c=rs.getString("category");
                       String pn=rs.getString("pilot_name");
                         String pp=rs.getString("pilot_phone");
                           
                     x++;
                     java.util.Vector r=new java.util.Vector();
                     r.add(f);
                     r.add(n);
                      r.add(c);
                       r.add(pn);
                      r.add(pp);

                     dtm.addRow(r);
                 }
             }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
             }
    }

